package com.example.javaproject3.week2.day4;

public class IncreaseOperator {
    public static void main(String[] args) {
        int i = 10;
        System.out.println("i : " + i++);
        System.out.println("i : " + i);

        System.out.println("i : " + ++i);
        System.out.println("i : " + i);

        int j = 10;
        System.out.println("j : " + j--);
        System.out.println("j : " + j);

        System.out.println("j : " + --j);
        System.out.println("j : " + j);
    }
}
