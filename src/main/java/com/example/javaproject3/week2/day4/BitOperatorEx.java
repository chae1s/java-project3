package com.example.javaproject3.week2.day4;

public class BitOperatorEx {
    public static void main(String[] args) {
        int val1= 1;
        System.out.println(val1 << 1);
        System.out.println(val1 << 2);

        int iVal2 = 32;
        System.out.println(iVal2 >> 1);
        System.out.println(iVal2 >> 2);
    }
}
