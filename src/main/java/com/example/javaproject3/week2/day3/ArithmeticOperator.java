package com.example.javaproject3.week2.day3;

public class ArithmeticOperator {
    public static void main(String[] args) {
        int iVal1 = 3;
        int iVal2 = 10;
        System.out.printf("a + b = %d\n", iVal1 + iVal2);
        System.out.printf("a - b = %d\n", iVal1 - iVal2);
        System.out.printf("a * b = %d\n", iVal1 * iVal2);
        System.out.printf("a / b = %d\n", iVal1 / iVal2);
        System.out.printf("a %% b = %d\n", iVal1 % iVal2);
    }
}
