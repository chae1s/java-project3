package com.example.javaproject3.codeup.num1000;

import java.util.Scanner;

public class Codeup10711073_2 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int num;
        do {
            num = sc.nextInt();
            if (num != 0) {
                System.out.println(num);
            }
        } while (num != 0);
    }
}
